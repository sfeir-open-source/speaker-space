package com.speakerspace.dto;

public class EmailDTO {
    private String email;
    private String uid;

    public EmailDTO() {}

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }
}